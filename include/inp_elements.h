struct inp_ElementInfo
{
    const char* type;
    int minNodesPerElement;  // min number of nodes including auxiliary ones
    int maxNodesPerElement;  // max number of nodes including auxiliary ones
};

///////////////////////////////////////////////////////////////////////////////
//
// Notes:
//  * struct inp_ElementInfo is temporary: use Jeltv info in eqc when available
//  * minNodesPerElement reflects minimum number of nodes used to describe
//    the element in the input (.inp) deck/file. ( => jeltv(ieltv_nnodu) )
//  * maxNodesPerElement reflects maximum number of nodes used to describe
//    the element plus number of auxiliary nodes defined in the same data
//    line as topology (eg. B32: 1,  1, 2, 3,  5. Node 5 is auxiliary and
//    defines the cross-section orientation
//
///////////////////////////////////////////////////////////////////////////////

